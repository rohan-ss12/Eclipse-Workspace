package stepDefinations;

import cucumber.api.java.en.And;
import cucumber.api.java.en.Given;
import cucumber.api.java.en.Then;
import cucumber.api.java.en.When;

public class sampleStepDefination {
	
	@Given("^User is on Landing Page$")
	public void user_is_on_landing_page() {
		
		System.out.println("User is on Landing Page");
		
	}
	@When("^User Login into application using Username and Password$")
	public void user_login_into_application_using_username_and_password() {
		
		System.out.println("User Login into application using Username and Password");
		
	}
	@Then("^Home Page is Populated$")
	public void home_page_is_populated() {
		
		System.out.println("Home Page is Populated");
		
	}
	@And("^Cart Details are Displayed$")
	public void cart_details_are_displayed() {
		
		System.out.println("Cart Details are Displayed");
		
	}

	

}
